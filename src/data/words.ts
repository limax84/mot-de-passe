function dedupe(list: string[]): string[] {
  return [...new Set(list)];
}

export const WORDS: Record<Difficulty, string[]> = {
  facile: dedupe(FACILE),
  moyen: dedupe(MOYEN),
  difficile: dedupe(DIFFICILE),
};

export function poolFor(difficulty: Difficulty | "melange"): string[] {
  if (difficulty === "melange") {
    return [...WORDS.facile, ...WORDS.moyen, ...WORDS.difficile];
  }
  return WORDS[difficulty];
}

/** Mélange de Fisher-Yates, sans mutation de l'entrée. */
export function shuffle<T>(list: T[], rng: () => number = Math.random): T[] {
  const copy = [...list];
  for (let i = copy.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}

export function drawWords(
  difficulty: Difficulty | "melange",
  count: number,
  exclude: Set<string> = new Set()
): string[] {
  const available = poolFor(difficulty).filter((w) => !exclude.has(w));
  return shuffle(available).slice(0, count);
}
